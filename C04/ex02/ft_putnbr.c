#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	arr[10];
	int		i;

	i = 0;
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	while (nb > 9)
	{
		arr[i] = (nb % 10) + '0';
		nb = nb / 10;
		i++;
	}
	if (nb <= 9 && nb != -2147483648)
		arr[i] = nb + '0';
	while (arr[i] != '\0')
	{
		write(1, &arr[i], 1);
		i--;
	}
}
